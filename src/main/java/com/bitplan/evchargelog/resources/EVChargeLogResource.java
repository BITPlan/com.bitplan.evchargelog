package com.bitplan.evchargelog.resources;

import java.util.Map;

import com.bitplan.persistence.Manager;
import com.bitplan.rest.resources.BaseResource;

/**
 * base class for NonOlet content
 * @author wf
 *
 * @param <MT>
 * @param <T>
 */
public class EVChargeLogResource <MT extends Manager<MT,T>,T> extends BaseResource<MT,T> {

  /**
   * prepare the root Map
   * @param title
   */
  public static void prepareMap(Map<String,Object> rootMap,String title) {
    rootMap.put("title", title);
    rootMap.put("tooltip", "Home");
    // TODO - add link to opensource URL
    rootMap.put("comment",
        "This page is generated by the com.bitplan.evchargetrack RESTful solution for electric car charge tracking. ");
  }
  
  /**
   * prepare the root map with the given title
   * @param title
   */
  public void prepareRootMap(String title) {
	  prepareMap(rootMap,title);
  }
}
